input_string = input("Enter numbers with ',' : ")
#.split разделя текста по определените символи и връща лист с елементите
numbers = input_string.split(", ")[::-1]
#[::-1] е разрязване на листа и -1 определя стъпката тоест връща целия лист ама в обратна пососка[начало:край:стъпка]
print(*numbers,sep=" ") 
#и последния ред * означава че разопаковаш листа и sep в принт е символа в който да разделя всеки елемент


 
